	ret
.valid2:
	
	ld	l,a
	ld	h,0 ; hl = dy + y
	add	hl,hl
	add	hl,hl
	add	hl,hl
	add	hl,hl
	add	hl,hl ; hl = (dy + y) * 32
	ld	a,d
	or	a,l
	ld	l,a ; hl = (dy + y) * 32 + dx + x
	
	ld	de,bump_intensity_table_1
	add	hl,de ; hl = bump_intensity_table + (dy + y) * 32 + dx + x
	ld	a,[hl]
	
	ld	de,32*32 ; bump_intensity_table_2 is next to bump_intensity_table_1
	add	hl,de
	ld	e,[hl]
	
	ld	d,a

	ret


;-------------------------------------------------------------------------------------

bump_map_handle:
	
	; Wait until maps are copied to VRAM
	
	ld	a,[bump_has_to_update_bgs_dma]
	and	a,a
	jr	z,.update
	
	halt
	jr	bump_map_handle
	
.update:
	
	; Calculate effect
	; ----------------
	
	ld	c,0 ; c = y
.loopy
	ld	b,0 ; b = x
.loopx
	push	bc
	
	;----
	
	; Get 4 subtiles
	
	sla	b
	sla	c
	
	push	bc
	call	bump_get_subtile_texture_from_coords	
	pop	bc
	push	de ; save tiles
	
	push	bc
	inc	b
	call	bump_get_subtile_texture_from_coords	
	pop	bc
	push	de ; save tiles

	inc	c
	push	bc
	call	bump_get_subtile_texture_from_coords
	pop	bc
	push	de ; save tiles
	
	inc	b
	call	bump_get_subtile_texture_from_coords
	
	; Reconstruct tile
	
	ld	a,d
	rla
	rla
	ld	b,a ; b = combined 1
	
	ld	a,e
	rla
	rla
	ld	c,a ; c = combined 2
	
	pop	de
	
	ld	a,b
	or	a,d
	rla
	rla
	ld	b,a ; b = combined 1
	
	ld	a,c
	or	a,e
	rla
	rla
	ld	c,a ; c = combined 2
	
	pop	de
	
	ld	a,b
	or	a,d
	rla
	rla
	ld	b,a ; b = combined 1
	
	ld	a,c
	or	a,e
	rla
	rla
	ld	c,a ; c = combined 2
	
	pop	de
	
	ld	a,b
	or	a,d
	ld	d,a ; d = combined 1
	
	ld	a,c
	or	a,e
	ld	e,a ; e = combined 2
	
	; d = combined 1
	; e = combined 2
	
	; Set tile to temp buffer
	
	pop	bc
	push	bc
	
	push	de
	ld	d,MAP_TEMP_1 >> 8
	ld	e,b ; de = base + x
	
	ld	l,c
	ld	h,$00 ; hl = y
	
	add	hl,hl
	add	hl,hl
	add	hl,hl
	add	hl,hl
	add	hl,hl ; hl = y * 32
	
	add	hl,de ; hl = base + x + (y * 32)
	pop	de

	ld	[hl],d ; write combined 1
	
	ld	a,e ; save combined 2
	
	ld	de,32*32 ; MAP_TEMP_2 is next to MAP_TEMP_1
	add	hl,de
	
	ld	[hl],a  ; write combined 2

	;----
	
	pop	bc
	
	inc	b
	ld	a,20
	cp	a,b
	jr	nz,.loopx
	
	inc	c
	ld	a,18
	cp	a,c
	jr	nz,.loopy
	
	; Tell the VBL handler to copy maps to VRAM
	
	ld	a,1
	ld	[bump_has_to_update_bgs_dma],a
	
	ret

;-------------------------------------------------------------------------------------

bump_map_update_bgs:
	
	ld	a,[bump_has_to_update_bgs_dma]
	and	a,a
	ret	z
	
	ld	a,0
	ld	[bump_has_to_update_bgs_dma],a
	
	ld	[rVBK],a
	DMA_COPY	MAP_TEMP_1,$9800,32*20,0 ; src, dst, size, is_hdma

	DMA_COPY	MAP_TEMP_2,$9C00,32*20,0 ; src, dst, size, is_hdma

	ret

;-------------------------------------------------------------------
;-                              OTHER                              -
;-------------------------------------------------------------------

bump_vbl_handler:
	
	ld	a,[rLCDC]
	xor	a,LCDCF_BG9C00
	ld	[rLCDC],a
	
	call	bump_palette_refresh
	
	call	bump_map_update_bgs
	
	LONG_CALL	gbt_update

	ret

;----------------------------------------------
	
	GLOBAL Bump
	
Bump:

	; ----	
	
	ld	a,1
	ld	[rVBK],a
	
	ld	bc,32*32*2 ; the 2 maps
	ld	d,0
	ld	hl,$9800
	call	vram_memset ; bc = size    d = value    hl = dest address

	; ----	
	
	call	bump_init_variables
	
	ld	a,0
	ld	[rVBK],a

	ld	bc,128
	ld	hl,bump_tiles
	ld	de,256 ;  de = start index
	call	vram_copy_tiles
	ld	bc,128
	ld	de,128 ;  de = start index
	call	vram_copy_tiles

	call	bump_map_handle
	
	call	wait_vbl
	
	call	bump_map_update_bgs
	
	ld	a,LCDCF_ON|LCDCF_BG8800|LCDCF_BG9800 ; configuration
	ld	[rLCDC],a
	
	; Configure IRQs
	
	ld	a,$01
	ld	[rIE],a
	
	ld	bc,bump_vbl_handler
	call	irq_set_VBL

	; Refresh screen

	call	wait_vbl
	
	; START	

.loop: ; Main loop
	
	call	bump_handle_events
	
	call	bump_map_handle

	call	wait_vbl
	
	ld	a,[bump_exit_demo]
	and	a,a
	jr	z,.loop

	; Exit...
	; -------
	
	call	demo_config_default
	
	ret

